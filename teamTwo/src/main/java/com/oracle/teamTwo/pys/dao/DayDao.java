package com.oracle.teamTwo.pys.dao;

public class DayDao {
	private String day;
	private String week;
	private String month;
	private String holiday_backgroundcolr;
	private String holiday_colr;
	private String format_time;

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public String getWeek() {
		return week;
	}

	public void setWeek(String week) {
		this.week = week;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getHoliday_backgroundcolr() {
		return holiday_backgroundcolr;
	}

	public void setHoliday_backgroundcolr(String holiday_backgroundcolr) {
		this.holiday_backgroundcolr = holiday_backgroundcolr;
	}

	public String getHoliday_colr() {
		return holiday_colr;
	}

	public void setHoliday_colr(String holiday_colr) {
		this.holiday_colr = holiday_colr;
	}

	public String getFormat_time() {
		return format_time;
	}

	public void setFormat_time(String format_time) {
		this.format_time = format_time;
	}

}
